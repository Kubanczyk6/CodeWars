package beta.whitespaceIsFun;

public class Solution {
    public static String multiply(int a, int b) {
        return Integer.toString(a*b);
    }

    public static String returnNegative(int a) {
        return String.valueOf(-Math.abs(a));
    }

    public static String evenOrOdd(int a) {
        return a%2==0?"Even":"Odd";
    }

    public static String sumOfPositive() {
        return "\n\n\n";
    }

    public static String reverseString() {
        return "\n\n\n";
    }
}
